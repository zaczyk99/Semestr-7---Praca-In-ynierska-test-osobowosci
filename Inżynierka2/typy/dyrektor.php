<h3>"Osobowość "Dyrektora"</h3>
<p class="tresc">Niezależny,  aktywny  i zdecydowany.  Racjonalny,  logiczny  i twórczy.  Dostrzega  szerszy  kontekst  analizowanych  problemów  i potrafi  przewidywać  przyszłe  konsekwencje  ludzkich  działań.  Cechuje  go  optymizm  oraz  zdrowe  poczucie  własnej  wartości.  Potrafi  przekształcać  teoretyczne  koncepcje  w konkretne,  praktyczne  plany  działania.

Wizjoner,  mentor  i organizator.  Posiada  naturalne  zdolności  przywódcze.  Jego  silna  osobowość,  krytycyzm  i bezpośredni  styl  często  onieśmiela  innych  ludzi  i powoduje  problemy  w kontaktach.

</p>


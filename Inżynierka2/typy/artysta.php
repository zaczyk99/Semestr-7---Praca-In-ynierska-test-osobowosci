<p class="tresc">Wrażliwy,  twórczy  i oryginalny.  Posiada  zmysł  estetyczny  i naturalne  zdolności  artystyczne.  Niezależny  — kieruje  się  własnym  systemem  wartości  i nie  ulega  naciskom.  Optymistyczny  i pozytywnie  nastawiony  do  życia;  potrafi  cieszyć  się  chwilą.


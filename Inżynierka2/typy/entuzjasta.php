<h3>"Osobowość "Entuzjasty"</h3>
<p class="tresc">Energiczny,  entuzjastyczny  i optymistyczny.  
Potrafi  cieszyć  się  życiem  i wybiega  myślami  w przyszłość.  Dynamiczny,  błyskotliwy  i twórczy.  Lubi  ludzi  i ceni  szczere,  autentyczne  relacje.  Ciepły,  serdeczny  i emocjonalny.  Źle  znosi  krytykę.  Posiada  dar  empatii  i dostrzega  potrzeby,  uczucia  oraz  motywy  innych  ludzi.  Inspiruje  i zaraża  innych  entuzjazmem.

Lubi  być  w centrum  wydarzeń.  Jest  elastyczny  i potrafi  improwizować.  Ma  skłonność  do  idealistycznych  pomysłów.  Łatwo  się  rozprasza  i ma  problemy  z doprowadzaniem  spraw  do  końca.

</p>


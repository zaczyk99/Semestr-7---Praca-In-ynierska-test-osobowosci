   <p4>Słabe strony</p4>
	<ul>
<li>•Entuzjaści często  mają  problemy  z określaniem  priorytetów  i koncentracją  na  realizowanych  zadaniach</li>
<li>•Zwykle  rozpoczynają  pracę  z entuzjazmem,  ale  łatwo  się  rozpraszają  i trudno  im  doprowadzić  ją  do  końca</li>
<li>•Zdarza  się,  że  nie  wywiązują  się  ze  złożonych  obietnic,  nie  dotrzymują  ustalonych  terminów  lub  zwlekają  z realizacją  zadań</li>
<li>•Mają  też  problemy  z zarządzaniem  czasem  i planowaniem</li>
<li>•Nie  najlepiej  radzą  sobie  z codziennymi,  powtarzalnymi  czynnościami  w życiu  prywatnym  oraz  rutynowymi  obowiązkami  w pracy</li>
<li>•Nie  potrafią  docenić  i wykorzystać  konstruktywnej  krytyki,  zwykle  postrzegają  ją  jako  atak  na  ich  osobę  lub  podważanie  ich  wartości</li>
<li>•Są  bardzo  uzależnieni  od  oceny  ludzi  i źle  znoszą  wszelkie  niepochlebne  uwagi  oraz  uszczypliwe  komentarze</li>
<li>•Za  wszelką  cenę  próbują  unikać  konfliktów  i nieprzyjemnych  rozmów  — zwykle  wolą  przemilczeć  problem,  niż  stawić  mu  czoła</li>
<li>•Mają  trudności  z wyrażaniem  krytycznych  opinii  i zwracaniem  innym  uwagi</li>
<li>•Duszą  w sobie  negatywne  emocje</li>
<li>•Koncentrując  się  na  potrzebach  innych  ludzi,  często  zapominają  o własnych</li>
<li>•Bywają  łatwowierni  i niekiedy  są  wykorzystywani  przez  innych</li>
<li>•Ich  entuzjazm  i skłonność  do  patrzenia  na  świat  przez  różowe  okulary  prowadzi  niekiedy  do  oderwania  od  

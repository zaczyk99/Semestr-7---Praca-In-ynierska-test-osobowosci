<li>•Chętnie  dzielą  się  swoją  wiedzą  i doświadczeniem  z innymi  ludźmi,  pomagając  im  w rozwiązywaniu  konkretnych  problemów</li>
<li>•Potrafią  w zrozumiały,  rzeczowy  sposób  wyrażać  swoje  myśli  i przekonywać  innych  do  swoich  racji</li>
